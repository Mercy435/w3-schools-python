price = 49
txt = "The price is {} dollars"
print(txt.format(price))
print()

txt = "The price is {:.3f} dollars" #this converts to float
print(txt.format(price))

quantity = 3
itemno = 567
price = 49
myorder = "I want {} pieces of item number {} for {:.2f} dollars."
